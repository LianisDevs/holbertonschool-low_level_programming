#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - adds 2 ints
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 ints
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiples 2 ints
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 ints
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulo 2 ints
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
